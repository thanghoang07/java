package lab03;

public class SinhVien {
	private int maSoSinhVien;
	private String hoTen;
	private NgaySinh ngaySinh;
	private int diemTrungBinh;
	private Lop lop;
	
	public SinhVien(
			
			int maSoSinhVien,
			String hoTen,
			NgaySinh ngaySinh,
			int diemTrungBinh,
			Lop lop
			
			) {
		
		this.maSoSinhVien = maSoSinhVien;
		this.hoTen = hoTen;
		this.ngaySinh = ngaySinh;
		this.diemTrungBinh = diemTrungBinh;
		this.lop = lop;
				
	}

	
	
}
